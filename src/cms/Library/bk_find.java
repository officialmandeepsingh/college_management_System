    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // codind for finding a book
        
        
       if(jTextField1.getText().isEmpty()||jTextField1.getText().length()<2)
       {
       JOptionPane.showMessageDialog(rootPane, " Enter a Name of Book");
       }
       else
       {
        DefaultTableModel m=(DefaultTableModel)jTable1.getModel();
        String Book_name=jTextField1.getText();
        try
        {
        Connection conn=DriverManager.getConnection(URL,USER,PASS);
       String q1="select * from books where book_name like ?";
       PreparedStatement s1=conn.prepareStatement(q1);
       s1.setString(1, "%"+jTextField1.getText()+"%");
        ResultSet rs=s1.executeQuery();
        while(rs.next())
           {
               m.setRowCount(0);
            String book_id=rs.getString("Book_Id").toUpperCase();
            String book_name=rs.getString("Book_Name").toUpperCase();
            String book_author=rs.getString("Book_Author").toUpperCase();
            String book_publisher=rs.getString("Book_Publisher").toUpperCase();
            String book_edition=rs.getString("Book_Edition").toUpperCase();
            String book_quantity=rs.getString("Book_Quantity").toUpperCase();
          m.addRow(new Object[]{book_id,book_name,book_author,book_publisher,book_edition,book_quantity});
            }
        }
        catch(Exception e)
        {
        JOptionPane.showMessageDialog(rootPane,"error : "+e.getMessage());
        }
       }
    }//GEN-LAST:event_jButton1ActionPerformed

