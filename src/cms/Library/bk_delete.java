    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String dlt=jTextField1.getText();
        int y=JOptionPane.showConfirmDialog(null," Do You Want To Delete a Book From Library ?   ");
        if(dlt.isEmpty())
        {
        JOptionPane.showMessageDialog(null," Enter a Book Name / Book ID");
        }
        else
        {
        if(y== JOptionPane.YES_OPTION)
        {
            try
            {
              
                Connection conn=DriverManager.getConnection(URL,USER,PASS);
                
                String q1="delete from books where Book_Name=?";
                PreparedStatement smt=conn.prepareStatement(q1);
                smt.setString(1, jTextField6.getText());
                int rs=smt.executeUpdate();
                if(rs==1)
                {
               JOptionPane.showMessageDialog(null,"Book Deleted ... ");
                clear();
                }
            }
            catch(Exception e)
            {
               JOptionPane.showMessageDialog(null," "+e.getMessage());
            }
        }}
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTextField6KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField6KeyTyped
        // TODO add your handling code here:
        char c=evt.getKeyChar();
        jLabel3.setText(null);
        if(!(Character.isAlphabetic(c) || (c==KeyEvent.VK_BACK_SPACE|| c==KeyEvent.VK_DELETE || c==KeyEvent.VK_SPACE)))
        {
            evt.consume();
            jLabel3.setForeground(Color.red);
            jLabel3.setText(" Enter Only Alphabetic (a-z) ");
        }
    }//GEN-LAST:event_jTextField6KeyTyped

