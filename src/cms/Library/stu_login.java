    private void jPasswordField4KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPasswordField4KeyTyped
        // TODO add your handling code here:
        char c=evt.getKeyChar();
        jLabel3.setText(null);
        if(!(Character.isDigit(c) || (c==KeyEvent.VK_BACK_SPACE|| c==KeyEvent.VK_DELETE || c==KeyEvent.VK_SPACE)))
        {
            evt.consume();
            //jLabel9.setForeground(Color.red);
            JOptionPane.showMessageDialog(null," Enter Only Digits(0-9) ");
        }
    }//GEN-LAST:event_jPasswordField4KeyTyped

